package ch01;

public class BitLogicExam {

	public static void main(String[] args) {
		// 비트연산자
		System.out.println("45 & 25 = " + (45&25)); //논리곱
		System.out.println("45 | 25 = " + (45|25)); //논리합
		System.out.println("45 ^ 25 = " + (45^25)); //베타적 논리합
		System.out.println("~45 = " + (~45));
		
	}
	
	
    
}

