package ch02;

public class ForExam {

	public static void main(String[] args) {
		// for문 을 연습해보기
		for(int i = 1; i <= 10 ; i++) {
			System.out.println("1~10까지 출력 : " + i );
			
		} //1~10까지 출력문 종료
		System.out.println("=======================");
		for(int w = 2 ; w <= 10 ; w = w + 2) {
			System.out.println("1~10까지 짝수만 출력 : " + w);
		} //1~10까지 짝수만 출력문 종료
		} //main 메서드 종료

	}//class 종료


